function addcart(id, i) {
  if (productIncart.length == 0) {
    data[i].qty = +1;
    productIncart.push(data[i]);
  } else {
    var flag = false;
    productIncart.map((e) => {
      if (e.id == id) {
        e.qty = +e.qty + 1;
        flag = true;
      }
    });

    if (!flag) {
      data[i].qty = +1;
      productIncart.push(data[i]);
    }
  }

  // productIncart.push(product[0]);
  localStorage.setItem("newcart", JSON.stringify(productIncart));
}

